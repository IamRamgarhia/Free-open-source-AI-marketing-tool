#!/usr/bin/env bash
# AdForge one-click installer for Mac / Linux.
# Run:  bash install.sh

set -e
cd "$(dirname "$0")"

echo
echo "=================================================="
echo " AdForge installer"
echo " by Dicecodes"
echo "=================================================="
echo

if ! command -v node >/dev/null 2>&1; then
  echo "[ERROR] Node.js is not installed."
  echo
  echo "Install Node 20+ from https://nodejs.org/en/download"
  echo "Or with a version manager:"
  echo "    macOS:        brew install node"
  echo "    Ubuntu/Deb:   sudo apt install nodejs npm"
  echo "    nvm:          nvm install 20 && nvm use 20"
  echo
  exit 1
fi

NODE_VER=$(node --version)
echo "Found Node $NODE_VER"
echo

echo "Installing dependencies (this may take a few minutes the first time)..."
echo
npm install --no-audit --no-fund

mkdir -p data
chmod +x start.sh stop.sh scripts/set-domain.sh 2>/dev/null || true

echo
echo "=================================================="
echo " Pick a port for the web app."
echo "=================================================="
echo
echo "The web app needs an open port on your machine."
echo "Common ports: 3000, 3005, 4000, 8080."
echo "If you have nothing else running, just press Enter for 3005."
echo

# Pre-fill from existing .env.local if present
DEFAULT_PORT=3005
if [ -f .env.local ]; then
  EXISTING=$(grep -E '^PORT=' .env.local | head -1 | cut -d= -f2)
  if [ -n "$EXISTING" ]; then
    DEFAULT_PORT="$EXISTING"
    echo "Existing .env.local found. Current PORT=$DEFAULT_PORT"
  fi
fi

read -p "Port [$DEFAULT_PORT]: " USERPORT
PORT="${USERPORT:-$DEFAULT_PORT}"

# Validate
if ! [[ "$PORT" =~ ^[0-9]+$ ]]; then
  echo "[WARN] \"$PORT\" is not a number. Falling back to 3005."
  PORT=3005
fi
if [ "$PORT" -lt 1024 ]; then
  echo "[WARN] Ports under 1024 need root privileges. Falling back to 3005."
  PORT=3005
fi
if [ "$PORT" -gt 65535 ]; then
  echo "[WARN] Port must be under 65536. Falling back to 3005."
  PORT=3005
fi

cat > .env.local <<EOF
# AdForge configuration (generated by install.sh)
PORT=$PORT
ADFORGE_SYNC_PORT=3006
EOF

echo
echo "Saved PORT=$PORT to .env.local"
echo
echo "=================================================="
echo " Install complete."
echo "=================================================="
echo
echo "Next steps:"
echo "  1. Run:  bash start.sh"
echo "  2. Open one of these in your browser:"
echo "       http://localhost:$PORT/"
echo "       http://adforge.localhost:$PORT/    (zero setup, works in all modern browsers)"
echo "  3. Follow the 5-step onboarding wizard"
echo
echo "Want a prettier URL like  http://adforge.local:$PORT/  ?"
echo "  Run:  sudo bash scripts/set-domain.sh"
echo
echo "Your data lives in:"
echo "  $(pwd)/data/snapshot.json"
echo "Zip this folder to move AdForge + your work to another machine."
echo
